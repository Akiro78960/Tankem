# -*- coding: utf-8 -*-
class DTOenregistrementProjectile:
    def __init__(self, time_sec, pos_x, pos_y, pos_z, en_mouvement):
        self.time_sec = time_sec
        self.pos_x = pos_x
        self.pos_y = pos_y
        self.pos_z = pos_z
        self.en_mouvement = en_mouvement

def getTime():
    return self.time_sec

def getX():
    return self.pos_x

def getY():
    return self.pos_y

def getZ():
    return self.pos_z

def getEnMouvement():
    return self.en_mouvement

