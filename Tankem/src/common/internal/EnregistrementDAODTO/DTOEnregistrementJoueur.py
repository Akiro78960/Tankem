#-*- coding: utf-8 -*-
class DTOenregistrementJoueur:
    def __init__(self, time_sec, pos_x, pos_y):
        self.time_sec = time_sec
        self.pos_x = pos_x
        self.pos_y = pos_y

    def getTime():
        return self.time_sec

    def getX():
        return self.pos_x

    def getY():
        return self.pos_y
